    <script>
        document.addEventListener('DOMContentLoaded', function() {
    // Animação de scroll para os elementos
    const animateOnScroll = function() {
        const elements = document.querySelectorAll('.fade-in');
        
        elements.forEach(element => {
            const elementPosition = element.getBoundingClientRect().top;
            const windowHeight = window.innerHeight;
            
            if (elementPosition < windowHeight - 100) {
                const delay = element.getAttribute('data-delay') || 0;
                
                setTimeout(() => {
                    element.classList.add('animate');
                }, delay);
            }
        });
    };

    // Adiciona evento de scroll
    window.addEventListener('scroll', animateOnScroll);
    
    // Executa uma vez ao carregar a página
    animateOnScroll();

    // Efeito hover para os cards
    const productCards = document.querySelectorAll('.product-card');
    
    productCards.forEach(card => {
        card.addEventListener('mouseenter', function() {
            this.style.transform = 'translateY(-10px)';
            this.style.boxShadow = '0 15px 30px rgba(0, 0, 0, 0.12)';
        });
        
        card.addEventListener('mouseleave', function() {
            if (!this.classList.contains('animate')) return;
            this.style.transform = 'translateY(0)';
            this.style.boxShadow = '';
        });
    });

    // Simulação de quick view (pode ser substituído por um modal real)
    const quickViewButtons = document.querySelectorAll('.quick-view');
    
    quickViewButtons.forEach(button => {
        button.addEventListener('click', function(e) {
            e.preventDefault();
            const productTitle = this.closest('.product-card').querySelector('.product-title').textContent;
            alert(`Visualização rápida: ${productTitle}\n\nEsta funcionalidade pode ser substituída por um modal real com mais detalhes do produto.`);
        });
    });
});
